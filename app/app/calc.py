"""
Calculator functions
"""


def add(x, y):
    """Add x and y and return result."""
    return x + y


def subtract(x, y):
    """Subtracting x and y and return result"""
    return y - x


def multiply(x, y):
    """
    Multiplying x and y and return result
    :param x:
    :param y:
    :return: res
    """
    return x * y
